namespace Kw.Aspects
{
    /// <summary>
    /// List of aspect roles used in <see cref="Kw.Aspects"/>.
    /// </summary>
    public static class BasicRoles
    {
        /// <summary>
        /// Algorithms and flow of control.
        /// </summary>
        public const string Control = "Control";

        /// <summary>
        /// Various conditions of execution.
        /// </summary>
        public const string Condition = "Condition";

        /// <summary>
        /// Composition of objects.
        /// </summary>
        public const string Composition = "Composition";

        /// <summary>
        /// Validation of field, property, or parameter value.
        /// </summary>
        public const string Validation = "Validation";

        /// <summary>
        /// Tracing and logging.
        /// </summary>
        public const string Tracing = "Tracing";
    }
}
